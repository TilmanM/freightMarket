package utils;

public class CoordinateImpl implements Coordinate{

	private double x;
	private double y;
	
	public CoordinateImpl(){
		
	}
	
		
	public void setX(double x) {
		this.x = x;
	}

	public void setY(double y) {
		this.y = y;
	}

	public double getX() {
		return x;
	}
	public double getY() {
		return y;
	}
	
	
	
	
}
